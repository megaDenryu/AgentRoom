import { describe, expect, it } from "vitest";
import { エージェント名 } from "../domain/エージェント名.js";
import { ルームID } from "../domain/ルームID.js";
import { メッセージストア } from "../infra/メッセージストア.js";
import { 新着通知ハブ } from "../infra/新着通知ハブ.js";
import { 新着を待つ } from "./新着待機.js";

const ルーム = ルームID.create("dev");
const AI1 = エージェント名.create("AI1");

describe("新着を待つ", () => {
  it("既に新着があれば待たずに返す", async () => {
    const ストア = メッセージストア.メモリ上に作る();
    const ハブ = new 新着通知ハブ();
    ストア.追加する(ルーム, AI1, "既存メッセージ");

    const 結果 = await 新着を待つ({
      ストア,
      ハブ,
      ルーム,
      基準連番: 0,
      タイムアウトミリ秒: 1000,
      取得上限: 100,
    });
    expect(結果.種別).toBe("新着あり");
  });

  it("待機中に通知が来たら新着を返す", async () => {
    const ストア = メッセージストア.メモリ上に作る();
    const ハブ = new 新着通知ハブ();

    const 待機 = 新着を待つ({
      ストア,
      ハブ,
      ルーム,
      基準連番: 0,
      タイムアウトミリ秒: 5000,
      取得上限: 100,
    });
    const 追加済み = ストア.追加する(ルーム, AI1, "待機中の新着");
    ハブ.通知する(追加済み);

    const 結果 = await 待機;
    expect(結果.種別).toBe("新着あり");
    if (結果.種別 === "新着あり") {
      expect(結果.メッセージ一覧[0]?.本文).toBe("待機中の新着");
    }
  });

  it("新着が無ければタイムアウトで返る", async () => {
    const ストア = メッセージストア.メモリ上に作る();
    const ハブ = new 新着通知ハブ();

    const 結果 = await 新着を待つ({
      ストア,
      ハブ,
      ルーム,
      基準連番: 0,
      タイムアウトミリ秒: 50,
      取得上限: 100,
    });
    expect(結果.種別).toBe("タイムアウト");
  });

  it("別ルームの通知では起きない", async () => {
    const ストア = メッセージストア.メモリ上に作る();
    const ハブ = new 新着通知ハブ();
    const 別ルーム = ルームID.create("another");

    const 待機 = 新着を待つ({
      ストア,
      ハブ,
      ルーム,
      基準連番: 0,
      タイムアウトミリ秒: 100,
      取得上限: 100,
    });
    const 別ルームの発言 = ストア.追加する(別ルーム, AI1, "別ルームの発言");
    ハブ.通知する(別ルームの発言);

    const 結果 = await 待機;
    expect(結果.種別).toBe("タイムアウト");
  });
});
