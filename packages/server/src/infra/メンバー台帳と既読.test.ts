import { describe, expect, it } from "vitest";
import { エージェント名 } from "../domain/エージェント名.js";
import { エージェント種別 } from "../domain/エージェント種別.js";
import { ルームID } from "../domain/ルームID.js";
import { 個別宛 } from "../domain/宛先.js";
import { 検証エラー } from "../domain/検証エラー.js";
import { メッセージストア } from "./メッセージストア.js";

const ルーム = ルームID.create("dev");
const AI1 = エージェント名.create("AI1");
const AI2 = エージェント名.create("AI2");
const 人間 = エージェント名.create("人間");

describe("メンバー台帳", () => {
  it("登録したメンバーが一覧に載り、再登録は冪等に種別だけ更新する", () => {
    const ストア = メッセージストア.メモリ上に作る();
    ストア.メンバーを登録する(ルーム, AI1, エージェント種別.create("claude-code"));
    const 初回 = ストア.メンバー一覧を取得する(ルーム);
    expect(初回).toHaveLength(1);
    expect(初回[0]?.名前.値).toBe("AI1");
    expect(初回[0]?.種別.値).toBe("claude-code");

    // 再登録: 種別が更新され、参加時刻は初回のまま、行は増えない
    ストア.メンバーを登録する(ルーム, AI1, エージェント種別.create("codex"));
    const 再登録後 = ストア.メンバー一覧を取得する(ルーム);
    expect(再登録後).toHaveLength(1);
    expect(再登録後[0]?.種別.値).toBe("codex");
    expect(再登録後[0]?.参加時刻ISO).toBe(初回[0]?.参加時刻ISO);
  });

  it("削除したメンバーは一覧から消える", () => {
    const ストア = メッセージストア.メモリ上に作る();
    ストア.メンバーを登録する(ルーム, AI1, エージェント種別.create("claude-code"));
    ストア.メンバーを登録する(ルーム, AI2, エージェント種別.create("codex"));
    ストア.メンバーを削除する(ルーム, AI1);
    const 一覧 = ストア.メンバー一覧を取得する(ルーム);
    expect(一覧).toHaveLength(1);
    expect(一覧[0]?.名前.値).toBe("AI2");
  });

  it("メンバーだけでメッセージが無いルームもルーム一覧に載る", () => {
    const ストア = メッセージストア.メモリ上に作る();
    ストア.追加する(ルーム, AI1, "メッセージのあるルーム");
    ストア.メンバーを登録する(ルームID.create("empty-room"), 人間, エージェント種別.create("human"));

    const 一覧 = ストア.ルーム一覧を取得する();
    expect(一覧.map((概要) => 概要.ルームID).sort()).toEqual(["dev", "empty-room"]);
    const 空ルーム = 一覧.find((概要) => 概要.ルームID === "empty-room");
    expect(空ルーム?.メッセージ数).toBe(0);
    expect(空ルーム?.最終連番).toBe(0);
  });
});

describe("未読数と既読位置", () => {
  it("未読数は宛先でフィルタされる（自分の発言・他人個別宛は数えない）", () => {
    const ストア = メッセージストア.メモリ上に作る();
    ストア.追加する(ルーム, AI1, "全員宛");
    ストア.追加する(ルーム, AI1, "AI2個別宛", 個別宛(AI2));
    ストア.追加する(ルーム, AI1, "人間個別宛", 個別宛(人間));
    ストア.追加する(ルーム, AI2, "AI2自身の発言");

    // AI2視点: 全員宛(1) + AI2個別宛(1)。人間個別宛と自分の発言は数えない
    expect(ストア.未読数を数える(ルーム, AI2)).toBe(2);
    // 人間視点: 全員宛(1) + 人間個別宛(1) + AI2の全員宛発言(1)
    expect(ストア.未読数を数える(ルーム, 人間)).toBe(3);
  });

  it("既読位置を進めると未読数が減り、小さい値では巻き戻らない", () => {
    const ストア = メッセージストア.メモリ上に作る();
    ストア.追加する(ルーム, AI1, "1通目");
    const 二通目 = ストア.追加する(ルーム, AI1, "2通目");
    const 三通目 = ストア.追加する(ルーム, AI1, "3通目");

    expect(ストア.未読数を数える(ルーム, AI2)).toBe(3);

    expect(ストア.既読位置を進める(ルーム, AI2, 二通目.連番)).toBe(二通目.連番);
    expect(ストア.未読数を数える(ルーム, AI2)).toBe(1);

    // 巻き戻し要求は無視され、現在値が返る
    expect(ストア.既読位置を進める(ルーム, AI2, 0)).toBe(二通目.連番);
    expect(ストア.未読数を数える(ルーム, AI2)).toBe(1);

    expect(ストア.既読位置を進める(ルーム, AI2, 三通目.連番)).toBe(三通目.連番);
    expect(ストア.未読数を数える(ルーム, AI2)).toBe(0);
  });

  it("負の連番は検証エラーになる", () => {
    const ストア = メッセージストア.メモリ上に作る();
    expect(() => ストア.既読位置を進める(ルーム, AI2, -1)).toThrow(検証エラー);
  });

  it("ルーム一覧は読者指定時に未読数を含み、未指定なら0", () => {
    const ストア = メッセージストア.メモリ上に作る();
    ストア.追加する(ルーム, AI1, "全員宛1");
    ストア.追加する(ルーム, AI1, "全員宛2");

    const 読者なし = ストア.ルーム一覧を取得する();
    expect(読者なし[0]?.未読数).toBe(0);

    const AI2視点 = ストア.ルーム一覧を取得する(AI2);
    expect(AI2視点[0]?.未読数).toBe(2);
  });
});

describe("宛先付きメッセージ", () => {
  it("宛先はDTOと再取得の両方に保持される", () => {
    const ストア = メッセージストア.メモリ上に作る();
    const 全員宛メッセージ = ストア.追加する(ルーム, AI1, "皆さんへ");
    const 個別宛メッセージ = ストア.追加する(ルーム, AI1, "AI2へ", 個別宛(AI2));

    expect(全員宛メッセージ.toJSON().宛先).toBeNull();
    expect(個別宛メッセージ.toJSON().宛先).toBe("AI2");

    const 再取得 = ストア.以降を取得する(ルーム, 0, 100);
    expect(再取得[0]?.宛先.種別).toBe("全員");
    expect(再取得[1]?.宛先.種別).toBe("個別");
    expect(再取得[1]?.toJSON().宛先).toBe("AI2");
  });
});
