// 送信者名から表示色を決定的に割り当てる。
// 色はあくまで補助で、送信者名ラベルを常時表示するのが本線（色覚特性への配慮）。
// パレットはライト背景上で白文字ラベルが読める濃色に限定している
const 配色パレット: readonly string[] = [
  "#1565c0",
  "#c62828",
  "#2e7d32",
  "#6a1b9a",
  "#e65100",
  "#00695c",
  "#ad1457",
  "#4e342e",
  "#37474f",
  "#827717",
];

export function 送信者色を割り当てる(送信者: string): string {
  let ハッシュ = 0;
  for (const 文字 of 送信者) {
    ハッシュ = (ハッシュ * 31 + (文字.codePointAt(0) ?? 0)) >>> 0;
  }
  const 色 = 配色パレット[ハッシュ % 配色パレット.length];
  return 色 ?? 配色パレット[0] ?? "#37474f";
}
