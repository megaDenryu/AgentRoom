// 稼働状況バッジの配色。色は補助であり、状態名テキストを常に併記する（種別配色.ts と同じ方針）。
// サーバーから来る値は「稼働中」「待機中」「不明」の3種のみ（サーバー側 domain/稼働表明.ts の
// 表示稼働状態値と対応）。未知の値が来ても落とさず既定色で表示する
const 稼働状況配色表: Readonly<Record<string, string>> = {
  稼働中: "#2e7d32",
  待機中: "#8078a4",
  不明: "#c9403a",
};

const 既定色 = "#546e7a";

export function 稼働状況色を返す(状態: string): string {
  return 稼働状況配色表[状態] ?? 既定色;
}
