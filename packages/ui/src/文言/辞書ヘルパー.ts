import type { ロケール } from "./ロケール";

// 各画面の文言辞書が共通で使う型基盤。日本語辞書オブジェクトの型を「あるべき形」の正とし、
// 英語辞書をその型で明示的に型付けすることで、tscの必須プロパティチェック+
// object literal excess property checkによりキーの過不足(訳し忘れ・訳しすぎ)を検出する。
//
// 使い方(画面ごとの文言ファイル):
//   const 日本語辞書 = { タイトル: "...", 段落一覧: ["...", "..."] };
//   type 画面文言 = typeof 日本語辞書;
//   const 英語辞書: 画面文言 = { タイトル: "...", 段落一覧: ["...", "..."] };
//   export function 画面文言を取得する(ロケール: ロケール): 画面文言 {
//     return ロケール別文言を取得する(ロケール, 日本語辞書, 英語辞書);
//   }
export function ロケール別文言を取得する<画面文言 extends object>(
  ロケール: ロケール,
  日本語辞書: 画面文言,
  英語辞書: 画面文言,
): 画面文言 {
  return ロケール === "ja" ? 日本語辞書 : 英語辞書;
}
