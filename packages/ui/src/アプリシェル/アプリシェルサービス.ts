import type { アクティビティID } from "vscode-shell-layout";
import type { 通知サービス, 通知許可状態 } from "../通知サービス";
import type { Relayクライアント } from "../通信/Relayクライアント";
import { キャラアクティビティ, 判定アクティビティ, ルームアクティビティ, 札場アクティビティ } from "./アクティビティ一覧";
import type { アプリシェル部品 } from "./アプリシェル部品";
import { 固定タブサービス } from "./固定タブサービス";
import { ルームタブ管理サービス } from "./ルームタブ管理サービス";

// 将来ルーム以外のタブを足しても衝突しないよう、タブIDには種別プレフィックスを付ける
// 札場・キャラは単一インスタンスのみ開く固定タブなので、ルームのような可変IDを持たない

function 許可状態の表示文言(状態: 通知許可状態): string {
  switch (状態) {
    case "granted":
      return "デスクトップ通知: 有効";
    case "denied":
      return "デスクトップ通知: ブラウザで拒否されています（効果音のみ）";
    case "default":
      return "デスクトップ通知: 未許可（効果音のみ）";
    case "unsupported":
      return "デスクトップ通知: この環境では使えません（効果音のみ）";
  }
}

export class アプリシェルサービス {
  private readonly _固定タブ: 固定タブサービス;
  private readonly _ルームタブ: ルームタブ管理サービス;

  private constructor(
    private readonly _部品: アプリシェル部品,
    private readonly _クライアント: Relayクライアント,
    private readonly _通知: 通知サービス,
  ) {
    this._固定タブ = new 固定タブサービス(_部品, _クライアント);
    this._ルームタブ = new ルームタブ管理サービス(_部品, _クライアント, _通知);
  }

  static 作る(依存: {
    部品: アプリシェル部品;
    クライアント: Relayクライアント;
    通知: 通知サービス;
  }): アプリシェルサービス {
    return new アプリシェルサービス(依存.部品, 依存.クライアント, 依存.通知);
  }

  ルームタブを開く(ルームID: string): void {
    this._ルームタブ.開く(ルームID);
  }

  // タブを閉じたらWS接続・タイマーも畳む
  タブを閉じる(タブid: string): void {
    this._ルームタブ.閉じる(タブid);
  }

  // タブ選択に追従して、サイドバーのメンバーパネル対象ルームを切り替える
  タブ選択を反映する(タブid: string): void {
    this._ルームタブ.選択を反映する(タブid);
  }

  // アクティビティバーのアイコンクリックの振り分け。「ルーム」はサイドバー開閉、
  // 「札場」は固定タブの開閉という別種の振る舞いを持つため、idで分岐する
  アクティビティを選択する(id: アクティビティID): void {
    if (id === 札場アクティビティ) {
      this._固定タブ.札場を開く();
      return;
    }
    if (id === キャラアクティビティ) {
      this._固定タブ.キャラを開く();
      return;
    }
    if (id === 判定アクティビティ) {
      this._固定タブ.判定を開く();
      return;
    }
    if (id === ルームアクティビティ) {
      // メニューバーを非表示運用にしているため（アプリシェル部品参照）、
      // サイドバーの開閉操作はこのアイコンが唯一の入口になる。
      // 狭幅では初期状態でサイドバーを閉じているため、開くための導線としてもここが必須
      this._部品.外殻.サイドバーを切り替える();
      void this._部品.サイドバー.更新する();
    }
  }

  // Fudaba（札場）のカンバンビューをタブとしてホストする。REST先(/api/fudaba/items)は
  // 同一オリジンの相対パスで組み立てられているため、AgentRoomサーバー(:7100)に
  // Fudabaルートが相乗りしている前提でそのまま届く（配線はホスト側main.tsが行う）
  // キャラ(人物)台帳の管理タブ。ワークスペース直下の第一級エンティティのため
  // ルームタブと違い引数を取らず、常に単一インスタンスで開閉する(札場タブと同じ発想)

  // サイドバーのメンバークリックを、選択中ルームタブの1対1フィルタ切替に流す
  相手フィルタを切り替える(名前: string): void {
    this._ルームタブ.相手フィルタを切り替える(名前);
  }

  async 通知許可を要求する(): Promise<void> {
    const 状態 = await this._通知.許可を要求する();
    this._部品.サイドバー.通知状態を表示する(許可状態の表示文言(状態));
  }

}
