import { 外殻レイアウト } from "vscode-shell-layout";
import { キャラアイコン, ルームアイコン, 札場アイコン } from "../アイコン";
import { 狭幅ブレークポイントpx } from "../レスポンシブ";
import { ルーム一覧サイドバー } from "../サイドバー/ルーム一覧サイドバー";
import { AgentRoomテーマ配色 } from "../テーマ";
import type { Relayクライアント } from "../通信/Relayクライアント";
import { キャラアクティビティ, ルームアクティビティ, 札場アクティビティ } from "./アクティビティ一覧";

// アプリシェルが集約する部品の型契約（部品DTO）。外殻レイアウトはコンストラクタで
// サイドバーを受け取るため、構築順ごとこのファクトリに閉じる。
// 会話UIが主役のためメニューバー・ステータスバーは非表示にし、AgentRoomに存在しない
// 設定画面のボタンも空配列指定で消す（構成とデザインの自由化、VscodeShellLayout README参照）
export class アプリシェル部品 {
  private constructor(
    readonly サイドバー: ルーム一覧サイドバー,
    readonly 外殻: 外殻レイアウト,
  ) {}

  static 作る(クライアント: Relayクライアント): アプリシェル部品 {
    const サイドバー = new ルーム一覧サイドバー(クライアント);
    const 外殻 = new 外殻レイアウト({
      タイトル: "AgentRoom",
      テーマ: AgentRoomテーマ配色,
      アクティビティ項目一覧: [
        { id: ルームアクティビティ, ラベル: "ルーム", アイコン: ルームアイコン },
        { id: 札場アクティビティ, ラベル: "札場", アイコン: 札場アイコン },
        { id: キャラアクティビティ, ラベル: "キャラ", アイコン: キャラアイコン },
      ],
      アクティビティバー下部項目一覧: [],
      メニューバー表示: false,
      ステータスバー表示: false,
      // 狭幅（スマホ・タブレット）ではアクティビティバーのテキストラベルを隠し、
      // アイコンのみにする（ラベルはツールチップへ）。LAN内スマホ・タブレット閲覧が
      // 主用途のAgentRoomではオプトインする。Jimbo/Pokemon等の他アプリは
      // 未指定のままなのでこのオプションの影響を受けない
      狭幅ではラベルを省略する: true,
      // 狭幅（スマホ・タブレット想定）では初期状態でサイドバーを閉じ、
      // メッセージ一覧＋送信欄が全幅で使えるようにする。メニューバーを非表示運用にしている
      // ため内蔵トグルは無く、開閉操作はアクティビティバーの「ルーム」アイコンが担う
      // （アプリシェルサービス.アクティビティを選択する() 参照）
      右サイドバー有効: window.innerWidth >= 狭幅ブレークポイントpx,
      パネル初期表示: false,
      右サイドバー内容: サイドバー,
    });
    return new アプリシェル部品(サイドバー, 外殻);
  }
}
