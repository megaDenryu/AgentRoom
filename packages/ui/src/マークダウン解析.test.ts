import { describe, expect, it } from "vitest";
import { インラインを解析する, 本文を解析する } from "./マークダウン解析";

describe("インラインを解析する", () => {
  it("太字とインラインコードと平文を分解する", () => {
    expect(インラインを解析する("これは**重要**で`code`です")).toEqual([
      { 種別: "平文", テキスト: "これは" },
      { 種別: "太字", テキスト: "重要" },
      { 種別: "平文", テキスト: "で" },
      { 種別: "インラインコード", テキスト: "code" },
      { 種別: "平文", テキスト: "です" },
    ]);
  });

  it("閉じの無い記号は平文として扱う", () => {
    expect(インラインを解析する("**閉じない")).toEqual([
      { 種別: "平文", テキスト: "**閉じない" },
    ]);
    expect(インラインを解析する("`閉じない")).toEqual([
      { 種別: "平文", テキスト: "`閉じない" },
    ]);
  });

  it("インラインコード内の太字記号は解釈しない（左端のトークンが勝つ）", () => {
    expect(インラインを解析する("`**そのまま**`")).toEqual([
      { 種別: "インラインコード", テキスト: "**そのまま**" },
    ]);
  });

  it("HTMLタグ文字列も単なるテキストとして保持する（エスケープは描画側のtextContentに任せる）", () => {
    expect(インラインを解析する('<script>alert("x")</script>')).toEqual([
      { 種別: "平文", テキスト: '<script>alert("x")</script>' },
    ]);
  });
});

describe("本文を解析する", () => {
  it("コードブロックと文章を分離する", () => {
    expect(本文を解析する("説明\n```\nconst x = 1;\n```\n続き")).toEqual([
      { 種別: "文章", インライン一覧: [{ 種別: "平文", テキスト: "説明" }] },
      { 種別: "コードブロック", コード: "const x = 1;" },
      { 種別: "文章", インライン一覧: [{ 種別: "平文", テキスト: "続き" }] },
    ]);
  });

  it("言語指定付きフェンスと閉じ忘れフェンスを扱える", () => {
    expect(本文を解析する("```ts\nlet a = 1;")).toEqual([
      { 種別: "コードブロック", コード: "let a = 1;" },
    ]);
  });

  it("複数行の文章は1ブロックにまとまり、改行が保持される", () => {
    const 結果 = 本文を解析する("1行目\n2行目");
    expect(結果).toEqual([
      { 種別: "文章", インライン一覧: [{ 種別: "平文", テキスト: "1行目\n2行目" }] },
    ]);
  });
});
