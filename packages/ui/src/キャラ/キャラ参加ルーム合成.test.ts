import { describe, expect, it } from "vitest";
import type { キャラDTO } from "../通信/キャラ型";
import type { キャラ稼働合成 } from "./キャラ稼働合成";
import { キャラ合成に参加ルームを付与する } from "./キャラ参加ルーム合成";

function キャラを作る(名前: string): キャラDTO {
  return {
    名前,
    種別: "AI",
    プロンプト: "",
    アイコンdataUrl: "",
    行動パターンメモ: "",
    作成者: "人間",
    作成時刻: "2026-07-14T00:00:00.000Z",
    更新時刻: "2026-07-14T00:00:00.000Z",
  };
}

function 合成を作る(名前: string): キャラ稼働合成 {
  return { キャラ: キャラを作る(名前), 稼働状態: null, 参加ルーム一覧: [] };
}

describe("キャラ合成に参加ルームを付与する", () => {
  it("マップに存在する名前には参加ルーム一覧を付与する", () => {
    const 結果 = キャラ合成に参加ルームを付与する(
      [合成を作る("案内役")],
      new Map([["案内役", ["room-a", "room-b"]]]),
    );
    expect(結果[0]?.参加ルーム一覧).toEqual(["room-a", "room-b"]);
  });

  it("マップに存在しない名前は空配列のままにする", () => {
    const 結果 = キャラ合成に参加ルームを付与する([合成を作る("孤立キャラ")], new Map());
    expect(結果[0]?.参加ルーム一覧).toEqual([]);
  });

  it("元の合成オブジェクトを変更せず新しいオブジェクトを返す", () => {
    const 元 = 合成を作る("案内役");
    const 結果 = キャラ合成に参加ルームを付与する([元], new Map([["案内役", ["room-a"]]]));
    expect(元.参加ルーム一覧).toEqual([]);
    expect(結果[0]).not.toBe(元);
  });

  it("並び順を保ったまま合成する", () => {
    const 結果 = キャラ合成に参加ルームを付与する(
      [合成を作る("A"), 合成を作る("B")],
      new Map([["B", ["room-a"]]]),
    );
    expect(結果.map((項目) => 項目.キャラ.名前)).toEqual(["A", "B"]);
    expect(結果[1]?.参加ルーム一覧).toEqual(["room-a"]);
  });
});
