import { describe, expect, it } from "vitest";
import type { キャラDTO } from "../通信/キャラ型";
import type { 稼働表明DTO } from "../通信/メッセージ型";
import { キャラ一覧に稼働状況を合成する } from "./キャラ稼働合成";

function キャラを作る(名前: string): キャラDTO {
  return {
    名前,
    種別: "AI",
    プロンプト: "",
    アイコンdataUrl: "",
    行動パターンメモ: "",
    作成者: "人間",
    作成時刻: "2026-07-14T00:00:00.000Z",
    更新時刻: "2026-07-14T00:00:00.000Z",
  };
}

function 稼働表明を作る(名前: string, 状態: 稼働表明DTO["状態"]): 稼働表明DTO {
  return { 名前, 状態, 現在の作業: null, 札ID: null, 更新時刻: "2026-07-14T00:00:00.000Z" };
}

describe("キャラ一覧に稼働状況を合成する", () => {
  it("同名の稼働表明があれば状態を合成する", () => {
    const 結果 = キャラ一覧に稼働状況を合成する(
      [キャラを作る("案内役")],
      [稼働表明を作る("案内役", "稼働中")],
    );
    expect(結果).toEqual([
      { キャラ: キャラを作る("案内役"), 稼働状態: "稼働中", 参加ルーム一覧: [] },
    ]);
  });

  it("同名の稼働表明が無ければ稼働状態はnull", () => {
    const 結果 = キャラ一覧に稼働状況を合成する([キャラを作る("案内役")], []);
    expect(結果).toEqual([
      { キャラ: キャラを作る("案内役"), 稼働状態: null, 参加ルーム一覧: [] },
    ]);
  });

  it("キャラの並び順を保ったまま合成する", () => {
    const 結果 = キャラ一覧に稼働状況を合成する(
      [キャラを作る("A"), キャラを作る("B")],
      [稼働表明を作る("B", "待機中")],
    );
    expect(結果.map((項目) => 項目.キャラ.名前)).toEqual(["A", "B"]);
    expect(結果[1]?.稼働状態).toBe("待機中");
  });
});
